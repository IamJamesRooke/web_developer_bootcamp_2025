/**
 * This module defines Joi validation schemas for the YelpCamp application.
 * It includes schemas for validating campground and review data.
 * Joi is used to ensure that the data meets the required structure and constraints.
 */

const Joi = require('joi');

module.exports.campgroundSchema = Joi.object({
    campground: Joi.object({
        title: Joi.string().required(),
        price: Joi.number().required().min(0),
        image: Joi.string().required(),
        location: Joi.string().required(),
        description: Joi.string().required()
    }).required()
});

module.exports.reviewSchema = Joi.object({
    review: Joi.object({
        rating: Joi.number().required().min(1).max(5),
        body: Joi.string().required(),
    })
})